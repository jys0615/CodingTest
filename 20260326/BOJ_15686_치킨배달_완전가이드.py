"""
================================================================
[BOJ 15686] 치킨 배달
완전탐색 + 맨해튼 거리 — 정석 가이드
================================================================

================================================================
STEP 1. 문제에서 "상태"와 "동작" 분리
================================================================

★ 코드 열기 전에 종이에 먼저 ★

[상태] — 기억해야 할 것
    ① 치킨집 좌표 리스트
    ② 집 좌표 리스트
    ③ 현재까지의 최솟값

[동작] — 순서대로 해야 할 일
    ① 입력 받으면서 치킨집/집 좌표 분리 저장
    ② 치킨집 중 M개 선택 (combinations)
    ③ 각 선택마다 모든 집의 치킨 거리 합 계산
    ④ 최솟값 갱신

================================================================
STEP 2. 핵심 개념 1 — 맨해튼 거리
================================================================

격자에서 벽이 없을 때 두 점 사이의 거리:

    거리 = |r1 - r2| + |c1 - c2|

BFS를 쓰면 안 되는 이유:
    → 이 문제는 지도에 벽이나 이동 불가 칸이 없습니다.
    → 어디든 자유롭게 이동 = 맨해튼 거리 한 줄로 끝.
    → BFS 쓰면 코드만 복잡해지고 틀릴 가능성만 늘어납니다.

    ✓  abs(hr - cr) + abs(hc - cc)
    ✗  BFS로 최단 경로 탐색

================================================================
STEP 3. 핵심 개념 2 — combinations (완전탐색)
================================================================

N개 중 M개를 고르는 모든 경우를 뽑습니다.
치킨집이 최대 13개이므로 C(13,6) = 1716가지 → 완전탐색 가능.

    from itertools import combinations

    items = [A, B, C, D]
    for combo in combinations(items, 2):
        # (A,B), (A,C), (A,D), (B,C), (B,D), (C,D)

================================================================
STEP 4. 치킨 거리 계산 — 자주 하는 실수
================================================================

각 집의 치킨 거리 = 선택된 치킨집들 중 "가장 가까운" 하나의 거리

    ✓  total += min(abs(hr-cr)+abs(hc-cc) for cr,cc in selected)
    ✗  total += sum(abs(hr-cr)+abs(hc-cc) for cr,cc in selected)

min을 빠뜨리면 선택된 모든 치킨집까지의 거리를 다 더하게 됩니다.
이게 이 문제에서 가장 자주 틀리는 실수입니다.

================================================================
STEP 5. 전체 흐름 — 4단계
================================================================

    입력 받으면서 좌표 분리
           ↓
    combinations으로 M개 조합 생성
           ↓
    각 조합마다: 모든 집의 치킨 거리 합 계산
    (각 집 → 선택된 치킨집 중 min 거리 → 합산)
           ↓
    전체 조합 중 최솟값 출력

================================================================
STEP 6. 완성 코드 (검증 완료)
================================================================
"""

from itertools import combinations
import sys
input = sys.stdin.readline

def solution():
    N, M = map(int, input().split())
    board = [list(map(int, input().split())) for _ in range(N)]

    # ----------------------------------------------------------
    # 1. 좌표 분리 — 매 조합마다 board 순회하면 낭비
    #    입력 받을 때 한 번만 분리해두기
    # ----------------------------------------------------------
    chickens = []   # 치킨집 좌표
    homes    = []   # 집 좌표

    for r in range(N):
        for c in range(N):
            if board[r][c] == 2:
                chickens.append((r, c))
            elif board[r][c] == 1:
                homes.append((r, c))

    # ----------------------------------------------------------
    # 2. 완전탐색: M개 조합마다 치킨 거리 합 계산
    # ----------------------------------------------------------
    min_dist = float('inf')

    for selected in combinations(chickens, M):
        total = 0

        for hr, hc in homes:
            # 이 집에서 선택된 치킨집 중 가장 가까운 거리
            # ★ min을 빠뜨리면 틀림 ★
            total += min(
                abs(hr - cr) + abs(hc - cc)
                for cr, cc in selected
            )

        min_dist = min(min_dist, total)

    print(min_dist)

solution()


