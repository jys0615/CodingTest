"""
[BOJ 14891] 톱니바퀴
오답노트 — 원본 코드 분석 + 수정

=========================================================
핵심 인덱스 구조 (외워두기)
=========================================================

        [6] [7] [0]
    [5]           [1]
    [4]           [2]
        [3] ← 아래

    - [0] : 12시 방향 (정답 채점에 사용)
    - [2] : 3시 방향  (오른쪽 톱니 — 오른쪽 바퀴의 [6]과 맞닿음)
    - [6] : 9시 방향  (왼쪽 톱니  — 왼쪽 바퀴의 [2]와 맞닿음)

    맞닿는 쌍:
        wheel[i][2]  ↔  wheel[i+1][6]
        (내 오른쪽)      (오른쪽 바퀴 왼쪽)

=========================================================
❌ 원본 코드의 버그 2가지
=========================================================

버그 1) 전파 판단과 실제 회전을 동시에 수행
    → 인접 바퀴를 먼저 돌려버리면,
      대상 바퀴를 돌릴 때 이미 바뀐 상태로 조건을 비교하게 됨.
    → 전파 여부 판단(체크)과 실제 회전(실행)을 반드시 분리해야 함.

    예시:
        원본: wheel[left] 먼저 돌림 → wheel[target] 나중에 돌림
                  ↑ 이 시점에 이미 wheel[left]가 바뀌어서 체인 체크가 꼬임

버그 2) 연쇄 전파가 없음 (1칸 옆만 체크)
    → 바퀴가 4개이므로 최대 3칸까지 연쇄될 수 있음.
    → ex) 바퀴 2 회전 → 바퀴 3도 → 바퀴 4도 연쇄 가능
    → 원본은 target 기준 좌우 1칸만 확인하고 끝남.

버그 3) 인접 바퀴 방향 반전 누락
    → 톱니가 맞물리면 인접 바퀴는 반대 방향으로 돌아야 함.
    → 원본은 direction 값을 그대로 넘기고 있음.

=========================================================
✅ 올바른 풀이 구조 (이 패턴을 외워둘 것)
=========================================================

핵심 원칙:
    1단계: 각 바퀴의 회전 방향을 미리 계산만 한다  (체크)
    2단계: 계산된 방향대로 한 번에 회전시킨다       (실행)

이 두 단계를 절대 섞지 않는다.
"""

from collections import deque
import sys
input = sys.stdin.readline

wheel = [deque(map(int, input().strip())) for _ in range(4)]
K = int(input().rstrip())

for _ in range(K):
    wheel_num, direction = map(int, input().split())
    target = wheel_num - 1

    # =========================================================
    # 1단계: 회전 방향 미리 계산 (실제로 돌리지 않음)
    # =========================================================
    directions = [0] * 4       # 0 = 안 돌림, 1 = 시계, -1 = 반시계
    directions[target] = direction

    # 왼쪽으로 연쇄 전파
    # wheel[i-1][2] : i-1번 바퀴의 오른쪽 톱니
    # wheel[i][6]   : i번 바퀴의 왼쪽 톱니
    # → 이 둘이 다를 때만 연쇄 (같으면 맞물려도 안 돌아감)
    for i in range(target, 0, -1):
        if wheel[i-1][2] != wheel[i][6]:
            directions[i-1] = -directions[i]  # 반드시 반대 방향
        else:
            break  # 같으면 더 이상 전파 없음

    # 오른쪽으로 연쇄 전파
    # wheel[i][2]   : i번 바퀴의 오른쪽 톱니
    # wheel[i+1][6] : i+1번 바퀴의 왼쪽 톱니
    for i in range(target, 3):
        if wheel[i+1][6] != wheel[i][2]:
            directions[i+1] = -directions[i]  # 반드시 반대 방향
        else:
            break

    # =========================================================
    # 2단계: 계산된 방향대로 한 번에 회전
    # =========================================================
    # 시계(1)   : 오른쪽에서 왼쪽으로 → pop() + appendleft()
    # 반시계(-1): 왼쪽에서 오른쪽으로 → popleft() + append()
    for i in range(4):
        if directions[i] == 1:
            wheel[i].appendleft(wheel[i].pop())
        elif directions[i] == -1:
            wheel[i].append(wheel[i].popleft())

# =========================================================
# 채점
# =========================================================
result = 0
for i in range(4):
    if wheel[i][0] == 1:
        result += (1 << i)   # 2**i 와 동일: 1, 2, 4, 8

print(result)


