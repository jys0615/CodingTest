import sys
input = sys.stdin.readline

# 물품의 수 N, 버틸 수 있는 무게 K 입력
# "준서가 여행에 가려 한다. 최대 K만큼의 무게만을 넣을 수 있는 배낭"
N, K = map(int, input().split())

item = []
for _ in range(N):
    W, V = map(int, input().split())
    item.append((W, V))
    # "각 물건은 무게 W와 가치 V를 가진다"
    # ex) item = [(6,13), (4,8), (3,6), (5,12)]

# "dp[i][j] = i번째 물건까지 고려했을 때, 무게 j를 버틸 수 있는 배낭의 최대 가치"
# "배낭에 넣을 수 있는 것들의 가치합이 최대가 되도록 하라"
dp = [[0] * (K+1) for _ in range(N+1)]
# dp[0][j] = 0 → 물건이 0개면 어떤 배낭이든 가치는 0

for i in range(1, N+1):       # i번째 물건을 고려
    w, v = item[i-1]          # 현재 물건의 무게(w)와 가치(v)

    for j in range(K+1):      # 배낭 용량 0 ~ K 까지 전부 계산
        
        if j < w:
            # "현재 물건의 무게가 배낭 용량보다 크면 넣을 수 없다" → 이전 물건까지의 최대 가치를 그대로 가져옴
            dp[i][j] = dp[i-1][j]
        
        else:
            # "넣을 수 있는 경우, 넣는 것과 안 넣는 것 중 더 큰 가치를 선택"
            dp[i][j] = max(
                dp[i-1][j],         # 현재 물건을 안 넣는 경우 → i-1번째 물건까지의 최대 가치 그대로
                dp[i-1][j-w] + v    # 현재 물건을 넣는 경우 → (배낭 용량 j에서 현재 무게 w를 뺀 공간의 최대 가치) + 현재 가치
            )

# "K만큼의 무게를 버틸 수 있는 배낭에 넣을 수 있는 가치의 최댓값"
# → N개 물건을 전부 고려하고, 용량이 K일 때의 최대 가치
print(dp[N][K])

